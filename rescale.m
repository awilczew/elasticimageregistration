function [ xx1,yx1,x,y ] = rescale( m,n, nodes, ix, iy,immov )
%UNTITLED2 Summary of this function goes here
%   Detailed explanation goes here

[x,y,ib,jb,ub,vb]=attributes1d(m,n, iy,ix);
glob=struct('ia',ib,'ja',jb,'ua',ub,'va',vb,'Bu0',B(ub,0),'Bu1',B(ub,1),...
    'Bu2',B(ub,2),'Bu3',B(ub,3),...
    'Bv0',B(vb,0),'Bv1',B(vb,1),'Bv2',B(vb,2),'Bv3',B(vb,3),...
    'dBu0',dB(ub,0),'dBu1',dB(ub,1),'dBu2',dB(ub,2),'dBu3',dB(ub,3),...
    'dB2u0',dB2(ub,0),'dB2u1',dB2(ub,1),'dB2u2',dB2(ub,2),'dB2u3',dB2(ub,3),...
    'dBv0',dB(vb,0),'dBv1',dB(vb,1),'dBv2',dB(vb,2),'dBv3',dB(vb,3),...
    'dB2v0',dB2(vb,0),'dB2v1',dB2(vb,1),'dB2v2',dB2(vb,2),'dB2v3',dB2(vb,3));
[imx1,xx1,yx1]=transformimage1d(nodes,immov,x,y,2*(m+4),n+4,glob);

end

